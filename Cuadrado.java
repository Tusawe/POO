public class Cuadrado {
	
	// atributos
	private int lado;
	
	// constructor
	// para crear cuadrados dado el lado.
	public Cuadrado (int lado) {
		
		this.lado = lado;
	
	}
	
	// getters y setters
	// getter para obtener valor del lado.
	public int getLado() {
	
		return lado;
	
	}
	
	public void setLado(int lado) {
		
		this.lado = lado;
	
	}

}
